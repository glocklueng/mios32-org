/////////////////////////////////////////////////////////////////////////////
// Include files
/////////////////////////////////////////////////////////////////////////////

#include <mios32.h>

#include "seq_morph.h"
#include "seq_cc.h"


/////////////////////////////////////////////////////////////////////////////
// Local variables
/////////////////////////////////////////////////////////////////////////////

static u8 morph_value;


/////////////////////////////////////////////////////////////////////////////
// Initialisation
/////////////////////////////////////////////////////////////////////////////
s32 SEQ_MORPH_Init(u32 mode)
{
  morph_value = 0;

  return 0; // no error
}


/////////////////////////////////////////////////////////////////////////////
// returns morph value (0..127)
/////////////////////////////////////////////////////////////////////////////
u8 SEQ_MORPH_ValueGet(void)
{
  return morph_value;
}

/////////////////////////////////////////////////////////////////////////////
// sets the morph value (0..127)
/////////////////////////////////////////////////////////////////////////////
s32 SEQ_MORPH_ValueSet(u8 value)
{
  if( value >= 128 )
    return -1; // invalid range

  morph_value = value;

  return 0; // no error
}
